    @Override
    public void update(Observable o, Object arg) {
            String acciones = (String)arg;
            txtAvisos.setText("");
            parser2.limpiarListas();// limpiar listas del parser2 para una nueva avaluacion
            txtMensaje.setText(acciones);//mensaje recibido desde el cliente
            
            //Evaluacion de acciones recibidas
            this.lexer.yyreset(new StringReader(acciones));
            try {
                    this.parser2.parse();
            } catch (Exception ex) {
                    Logger.getLogger(FrameServidor.class.getName()).log(Level.SEVERE, null, ex);
            }
            /*EVALUAR LAS ACCIONES*/
            controladorServ = new ControladorServidor(parser2, this);
            controladorServ.implementarAcciones();
            
            
    }
