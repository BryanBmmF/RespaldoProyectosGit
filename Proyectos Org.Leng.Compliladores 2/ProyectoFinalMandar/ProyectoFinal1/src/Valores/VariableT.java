public class VariableT {
    private String tipo;
    private String valorT;

    public VariableT(String tipo, String valorT) {
        this.tipo = tipo;
        this.valorT = valorT;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getValorT() {
        return valorT;
    }

    public void setValorT(String valorT) {
        this.valorT = valorT;
    }
    
    
}
