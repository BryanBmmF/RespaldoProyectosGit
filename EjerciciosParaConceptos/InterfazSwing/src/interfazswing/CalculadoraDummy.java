     private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
          int resultado;
		if (operacion == SUMA) {
			resultado = primerNumero + segundoNumero;
		} else {
			resultado = primerNumero - segundoNumero;
		}
		etiquetaResultados.setText(String.valueOf(resultado));
		esPrimerNumero = true;
     }//GEN-LAST:event_botonIgualActionPerformed

	private void asignarNumero(int numero) {
		if (esPrimerNumero) {
			primerNumero = numero;
			etiquetaResultados.setText("");
		} else {
			segundoNumero = numero;
		}
		concatenarTexto(String.valueOf(numero));
	}
	
	private void concatenarTexto(String valor) {
		String textoExistente = etiquetaResultados.getText();
		etiquetaResultados.setText(textoExistente + valor);
	}
	

